# Exercise 3 (CHALLENGE) — Number Analyzer

# Ask user for a number

# Compute:
#   square
#   cube
#   remainder when divided by 5
#   floor division by 3

user = float(input("Give me a number: "))

square = user ** 2
cube = user ** 3
remainder = user % 5
floor_div = user // 3

print(square)
print(cube)
print(remainder)
print(floor_div)